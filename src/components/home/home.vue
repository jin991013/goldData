<script>
import "./css/css";
export default {
    data: () => ({
        index: 0,
        time: ""
    }),
    watch: {
        index(newValue, oldValue) {
            this.tabCarouselChange(oldValue, newValue);
        }
    },
    methods: {
        interval() {
            if (this.index == 5) {
                this.index = 0;
            } else {
                this.index++;
            }
        },
        tabCarouselChange(oldIndex, newIndex) {
            $("#home-contant .carousel-list ul li")
                .eq(oldIndex)
                .removeClass("current");
            $("#home-contant .carousel-list ul li")
                .eq(newIndex)
                .addClass("current");
            $("#home-contant .carousel-contant-list li")
                .eq(oldIndex)
                .removeClass("current");
            $("#home-contant .carousel-contant-list li")
                .eq(newIndex)
                .addClass("current");
            $("#home-contant  .mycarousel-control ul li a")
                .eq(oldIndex)
                .removeClass("current");
            $("#home-contant  .mycarousel-control ul li a")
                .eq(newIndex)
                .addClass("current");
            if (newIndex > 1) {
                if (oldIndex - 2 < 0) {
                    oldIndex = 3;
                } else {
                    oldIndex -= 2;
                }
                newIndex -= 2;
                $("#home-contant .tab-contant-list li")
                    .eq(oldIndex)
                    .removeClass("current");
                $("#home-contant .tab-contant-list li")
                    .eq(newIndex)
                    .addClass("current");
            } else {
                $("#home-contant .tab-contant-list li")
                    .eq(3)
                    .removeClass("current");
            }
        }
    },
    mounted() {
        var that = this;
        this.time = setInterval(this.interval, 4000);
        $("#home-contant .tab-contant-list li").click(function(e) {
            that.index = parseInt($(this).attr("index")) + 2;
            clearInterval(that.time);
            that.time = setInterval(that.interval, 4000);
        });
    }
};
</script>

