export default {
  data:()=>({
    index:0
  }),
  watch: {
    index:function(newValue,oldValue){
      $('#my-system .tab-title-list li').eq(oldValue).removeClass('current');
      $('#my-system .tab-title-list li').eq(newValue).addClass('current');
      $('#my-system .tab-img-list li').eq(oldValue).removeClass('current');
      $('#my-system .tab-img-list li').eq(newValue).addClass('current');
    }
  },
  methods: {
    systemInterval(){
      if(this.index+1==8){
        this.index=0;
      }else{
        this.index++;
      }
    }
  },
  mounted() {
    let timer=setInterval(this.systemInterval,4000);
    let that=this;
    $('#my-system .tab-title-list li').click(function(){
      clearInterval(timer);
      that.index=parseInt($(this).attr('index'));
      timer=setInterval(that.systemInterval,4000);
    });
  }
}
